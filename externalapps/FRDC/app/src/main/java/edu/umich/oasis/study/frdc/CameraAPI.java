    private class CameraAPIThread extends Thread
    {
        public static final int MSG_SHOW_CAMERA_PREVIEW = 1;
        public static final int MSG_STOP_CAMERA_PREVIEW = 2;
        public static final int MSG_SHUTDOWN = 3;

        public Handler mHandler;

        public CameraAPIThread()
        {

        }

        public void run()
        {
            Looper.prepare();

            mHandler = new Handler() {
                public void handleMessage(Message m)
                {
                    switch(m.what)
                    {
                        case MSG_SHOW_CAMERA_PREVIEW:
                            internal_showCameraPreview();
                            break;
                        case MSG_STOP_CAMERA_PREVIEW:
                            internal_stopCameraPreview();
                            return;
                            //break;
                        case MSG_SHUTDOWN:
                            Looper.myLooper().quitSafely();
                            break;
                    }
                }

            };

            Looper.loop();
        }
    }

